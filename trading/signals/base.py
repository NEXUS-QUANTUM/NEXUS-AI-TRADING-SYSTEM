class SignalValidator:
    """
    Validator for signals.
    
    Provides methods to validate signal data and check signal quality.
    """
    
    @staticmethod
    def validate(signal: Signal) -> Tuple[bool, Optional[str]]:
        """
        Validate a signal.
        
        Args:
            signal: Signal to validate
            
        Returns:
            Tuple[bool, Optional[str]]: (is_valid, error_message)
        """
        try:
            signal._validate()
            return True, None
        except ValueError as e:
            return False, str(e)
    
    @staticmethod
    def is_quality_signal(signal: Signal, min_confidence: float = 0.5) -> bool:
        """
        Check if signal meets quality standards.
        
        Args:
            signal: Signal to check
            min_confidence: Minimum confidence threshold
            
        Returns:
            bool: True if signal meets quality standards
        """
        if not signal.is_actionable():
            return False
        
        if signal.confidence < min_confidence:
            return False
        
        if signal.strength == SignalStrength.WEAK:
            return False
        
        if signal.price <= 0:
            return False
        
        return True
    
    @staticmethod
    def get_quality_score(signal: Signal) -> float:
        """
        Calculate a quality score for the signal.
        
        Args:
            signal: Signal to score
            
        Returns:
            float: Quality score (0-1)
        """
        if not signal.is_actionable():
            return 0.0
        
        # Confidence contribution (0-0.4)
        confidence_score = signal.confidence * 0.4
        
        # Strength contribution (0-0.3)
        strength_scores = {
            SignalStrength.WEAK: 0.05,
            SignalStrength.MEDIUM: 0.15,
            SignalStrength.STRONG: 0.25,
            SignalStrength.VERY_STRONG: 0.30,
        }
        strength_score = strength_scores.get(signal.strength, 0.15)
        
        # Price reasonableness (0-0.1)
        if signal.price > 0:
            price_score = 0.1
        else:
            price_score = 0
        
        # Stop loss presence (0-0.1)
        stop_score = 0.1 if signal.stop_loss is not None else 0
        
        # Take profit presence (0-0.1)
        take_score = 0.1 if signal.take_profit is not None else 0
        
        return min(1.0, confidence_score + strength_score + price_score + stop_score + take_score)


