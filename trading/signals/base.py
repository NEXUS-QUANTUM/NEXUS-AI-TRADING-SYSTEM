class SignalType(str, Enum):
    """
    Types of trading signals.
    
    BUY: Signal to enter a long position
    SELL: Signal to enter a short position
    CLOSE: Signal to close an existing position
    NEUTRAL: No signal, wait
    HOLD: Maintain current position
    """
    BUY = "buy"
    SELL = "sell"
    CLOSE = "close"
    NEUTRAL = "neutral"
    HOLD = "hold"


class SignalStrength(str, Enum):
    """
    Strength of a trading signal.
    
    WEAK: Low conviction signal
    MEDIUM: Moderate conviction signal
    STRONG: High conviction signal
    VERY_STRONG: Maximum conviction signal
    """
    WEAK = "weak"
    MEDIUM = "medium"
    STRONG = "strong"
    VERY_STRONG = "very_strong"


class SignalSource(str, Enum):
    """
    Source of a trading signal.
    
    STRATEGY: Generated by a trading strategy
    INDICATOR: Generated by technical indicators
    PATTERN: Generated by pattern recognition
    AI: Generated by AI/ML models
    MANUAL: Manually entered by user
    EXTERNAL: Received from external source
    """
    STRATEGY = "strategy"
    INDICATOR = "indicator"
    PATTERN = "pattern"
    AI = "ai"
    MANUAL = "manual"
    EXTERNAL = "external"


